package org.opentutorials.javatutorials.numberstring;

public class Number {
    public static void main(String[] args){
        System.out.println(1.2+1.3);  //자바에서는 따옴표가 없는 숫자는 숫자로 인식한다.
        System.out.println(2+3);
        System.out.println(9*9); //곱하기를 할 때에는 에스터리스크 (*)를 사용한다.
        System.out.println(6/2); //나누기를 할 대에는 / 를 사용한다.
    }
}
